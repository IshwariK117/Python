# tuple: ordered collection of items enclosed in parantehsis separated by comma
tuple1 = ('a', 'b', 'c', 'd', 'e', 'f')
tuple2 = ("Red", "Green", "Blue")
print(tuple1)  # output:('a', 'b', 'c', 'd', 'e', 'f')
print(tuple2)  # output:('Red', 'Green', 'Blue')

details = ("Abhi", 18, "FY IT", 9.8)
print(details)
# output:('Abhi', 18, 'FY IT', 9.8)


# range of index
# Syntax:
# Tuple[start : end : jumpIndex]
nimals = ("cat", "dog", "bat", "mouse", "pig", "horse", "donkey", "goat", "cow")
print(animals[3:7])     #using positive indexes
print(animals[-7:-2])   #using negative indexes