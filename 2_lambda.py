# lambda is cstomizable variable ..we can do operation in different scenario
# perform all arithmetic operation

multiply = lambda a, b: a * b
result = multiply(2, 3)
print(result)

add = lambda a, b: a + b
result = add(2, 3)
print(result)

subtract = lambda a, b: a - b
result = subtract(3, 2)
print(result)

div = lambda a, b: a / b
result = div(10, 5)
print(result)

rem = lambda a, b: a % b
result = rem(10, 5)
print(result)
